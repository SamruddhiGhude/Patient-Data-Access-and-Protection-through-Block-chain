    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String doctor_name=jTextField1.getText();
        String mobile_number=jTextField2.getText();
        String email_id=jTextField3.getText();
        String reg_no=jTextField5.getText();
        String speciality=(String)jComboBox1.getSelectedItem();
        String username=jTextField4.getText();
        char pass[]=jPasswordField1.getPassword();
        String password=String.valueOf(pass);
        char conpass[]=jPasswordField2.getPassword();
        String conpassword=String.valueOf(conpass);
        
        Statement st=new DB_Driver().getStatement();
        
        if(doctor_name.isEmpty())
            JOptionPane.showMessageDialog(null, "Enter doctor name");
        else if(!isValidMobile(mobile_number))
            JOptionPane.showMessageDialog(null, "Enter Valid Mobile Number");
        else if(!isValidEmail(email_id))
            JOptionPane.showMessageDialog(null, "Enter Valid Email Id");
         else if(reg_no.isEmpty())
            JOptionPane.showMessageDialog(null, "Enter registration number");
        else if(speciality.equals("Select"))
            JOptionPane.showMessageDialog(null, "Select Speciality");
        else if(username.isEmpty())
            JOptionPane.showMessageDialog(null, "Enter username");
        else if(password.isEmpty())
            JOptionPane.showMessageDialog(null, "Enter Password");
        else if(conpassword.isEmpty())
            JOptionPane.showMessageDialog(null, "Enter confirm password");
        else if(!password.equals(conpassword))
            JOptionPane.showMessageDialog(null, "Password and confirm password is not matched");
        else
        {
            Doctor_Registration dr=new Doctor_Registration();
            if(dr.isDoctorRegistration(st, doctor_name, mobile_number, email_id,  reg_no, speciality, username, password))
                JOptionPane.showMessageDialog(null, "Doctor Registration Done Successfully");
            else 
                JOptionPane.showMessageDialog(null, "Error in doctor Registration");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

